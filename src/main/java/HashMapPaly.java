public class HashMapPaly {
    public static void main(String[] args){
        int hashCode = "nihao".hashCode();
        int Num1 = -15 >> 2;
        int Num2 = -15 >>> 2;
        String str = Integer.toBinaryString(-5);
        int Num6 = 4 >>> 16;
        System.out.println(Num6+".."+hashCode+".."+Num1+"..."+Num2+".."+str);
    }
}
